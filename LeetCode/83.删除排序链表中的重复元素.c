// @lc code=start
/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     struct ListNode *next;
 * };
 */


struct ListNode* deleteDuplicates(struct ListNode* head){
    if (!head) {
        return head;
    }
    struct ListNode* index = head;

    while (index->next) {
        if (index->val == index->next->val) {
            index->next = index->next->next;
        }
        else {
            index = index->next;
        }
    }
    
    return head;
}
